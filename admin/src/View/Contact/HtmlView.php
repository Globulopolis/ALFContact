namespace Joomla\Component\ALFContact\Administrator\View\Contact;

defined('_JEXEC') or die;

use Joomla\CMS\Factory;
use Joomla\CMS\Language\Text;
use Joomla\CMS\MVC\View\GenericDataException;
use Joomla\CMS\MVC\View\HtmlView as BaseHtmlView;
use Joomla\CMS\Toolbar\Toolbar;
use Joomla\CMS\Toolbar\ToolbarHelper;

class HtmlView extends BaseHtmlView
{
	protected $item;
	protected $form;

	/**
	 * Execute and display a template script.
	 *
	 * @param   string  $tpl  The name of the template file to parse; automatically searches through the template paths.
	 *
	 * @return  void
	 *
	 * @throws  \Exception
	 * @since   4.0
	 */
	public function display($tpl = null)
	{
		$this->item = $this->get('Item');
		$this->form = $this->get('Form');

		if (count($errors = $this->get('Errors')))
		{
			throw new GenericDataException(implode("\n", $errors), 500);
		}

		$this->addToolbar();

		parent::display($tpl);
	}

	/**
	 * Add the page title and toolbar.
	 *
	 * @return  void
	 *
	 * @since   4.0
	 *
	 * @throws  \Exception
	 */
	protected function addToolbar()
	{
		$app     = Factory::getApplication();
		$toolbar = Toolbar::getInstance();
		$isNew   = ($this->item->id == 0);

		$app->input->set('hidemainmenu', 1);

		$title = $isNew ? Text::_('COM_ALFCONTACT_MANAGER_ALFCONTACT_NEW') : Text::_('COM_ALFCONTACT_MANAGER_ALFCONTACT_EDIT');
		ToolbarHelper::title($title, 'fa fa-address-book');

		$toolbar->apply('contact.apply');
		$saveGroup = $toolbar->dropdownButton('save-group');
		$saveGroup->configure(
			function (Toolbar $childBar)
			{
				$childBar->save('contact.save');
				$childBar->save2new('contact.save2new');
			}
		);

		$toolbar->cancel('contact.cancel');
	}
}
